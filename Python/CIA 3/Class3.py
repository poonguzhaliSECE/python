#calculator
class cal:
    def add(self,a,b):
        print("Sum is:",a+b)
    def sub(self,a,b):
        print("Differnce is:",a-b)
    def multi(self,a,b):
        print("Product is:",a*b)
    def div(self,a,b):
        print("Quotient is:",a/b)
c=cal()
c.add(5,5)
c.sub(5,5)
c.multi(5,5)
c.div(5,5)